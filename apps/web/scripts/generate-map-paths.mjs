const EXPECTED_STATE_COUNT = 51;

const SIMPLIFY_THRESHOLD = 0.005;
const COORD_PRECISION = 1;

function loadStateFeatures() {
	const topology = JSON.parse(readFileSync(US_ATLAS_PATH, 'utf-8'));
	const presimplified = presimplify(topology);
	const simplified = simplify(presimplified, SIMPLIFY_THRESHOLD);
	return feature(simplified, simplified.objects.states).features;
}

function projectStates(features) {
	const pathGenerator = geoPath(geoAlbersUsa()).digits(COORD_PRECISION);
	const paths = {};
	const skipped = [];
	for (const stateFeature of features) {
		const fips = String(stateFeature.id).padStart(2, '0');
		const usps = FIPS_TO_USPS[fips];
		// us-atlas includes US territories (FIPS 60, 66, 69, 72, 78) that
		// are outside the scope of this project, skip them.
		if (usps === undefined) {
			skipped.push(fips);
			continue;
		}
		const d = pathGenerator(stateFeature);
		if (d === null) {
			throw new Error(`Path generator returned null for ${usps} (FIPS ${fips})`);
		}
		paths[usps] = d;
	}
	if (skipped.length > 0) {
		console.log(`Skipped territories (FIPS): ${skipped.sort().join(', ')}`);
	}
	return paths;
}

function renderTypeScript(paths) {
	const sortedUsps = Object.keys(paths).sort();
	if (sortedUsps.length !== EXPECTED_STATE_COUNT) {
		throw new Error(`Expected ${EXPECTED_STATE_COUNT} states + DC, got ${sortedUsps.length}`);
	}
	const pathEntries = sortedUsps.map((code) => `\t${code}: ${JSON.stringify(paths[code])}`);
	return [
		'// Auto-generated by scripts/generate-map-paths.mjs — do not hand-edit.',
		'// To regenerate: `pnpm gen:map` from apps/web/.',
		'',
		'export const MAP_VIEWBOX = {',
		`\twidth: ${VIEWBOX_WIDTH},`,
		`\theight: ${VIEWBOX_HEIGHT}`,
		'} as const;',
		'',
		'export const STATE_PATHS: Readonly<Record<string, string>> = {',
		pathEntries.join(',\n'),
		'};',
		''
	].join('\n');
}

function main() {
	const features = loadStateFeatures();
	const paths = projectStates(features);
	const output = renderTypeScript(paths);
	writeFileSync(OUTPUT_PATH, output, 'utf-8');
	console.log(`Wrote ${OUTPUT_PATH} (${Object.keys(paths).length} state paths).`);
}

main();
