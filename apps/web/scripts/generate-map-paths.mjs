/**
 * Generate apps/web/src/lib/states/map-paths.s from us-atlas TopoJSON.
 *
 * Reads node_modules/us-atlas/states-50m.json, projects each state with
 * geoAlbersUsa, and writes a typed constant of SVG path strings keyed by
 * USPS state code.
 *
 * Run: `pnpm gen:map` from apps/web/.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { createRequire } from 'node:module';
import { geoAlbersUsa, geoPath } from 'd3-geo';
import { feature } from 'topojson-client';
import { presimplify, simplify } from 'topojson-simplify';

const __dirname = dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);

// us-atlas state geometry at 10m resolution
const US_ATLAS_PATH = require.resolve('us-atlas/states-10m.json');
const OUTPUT_PATH = join(__dirname, '..', 'src', 'lib', 'states', 'map-paths.ts');

const VIEWBOX_WIDTH = 975;
const VIEWBOX_HEIGHT = 610;

