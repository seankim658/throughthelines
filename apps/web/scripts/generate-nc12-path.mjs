/**
 * Generate apps/web/src/lib/hero/nc12-path.ts from the pinned Lewis GeoJSON.
 *
 * Reads North Carolina's 12th congressional district (113th–114th Congress —
 * the 2011 plan later struck down in Cooper v. Harris), simplifies it to a
 * light decorative outline, projects it with geoMercator, crops the viewBox
 * tight to the shape, and writes a typed module with the viewBox and SVG path
 * string used by the homepage hero.
 *
 * Run: `pnpm gen:nc12` from apps/web/.
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { geoMercator, geoPath } from 'd3-geo';
import { topology } from 'topojson-server';
import { presimplify, simplify, quantile } from 'topojson-simplify';
import { feature } from 'topojson-client';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Pinned Lewis source, relative to the repo root
const INPUT_PATH = join(
	__dirname,
	'..',
	'..',
	'..',
	'data',
	'raw',
	'lewis',
	'NC',
	'North Carolina_113_to_114.geojson'
);
const OUTPUT_PATH = join(__dirname, '..', 'src', 'lib', 'hero', 'nc12-path.ts');

const DISTRICT = 12;
const FIT_SIZE = 1000; // nominal box the projection fits into before cropping to bounds
const COORD_PRECISION = 1; // decimal places in the emitted path, matches gen:map
const KEEP_FRACTION = 0.03; // keep ~3% of the original points

function loadDistrictFeature() {
	const collection = JSON.parse(readFileSync(INPUT_PATH, 'utf-8'));
	const match = collection.features.find((f) => f.properties.district === DISTRICT);
	if (match === undefined) {
		throw new Error(`District ${DISTRICT} not found in ${INPUT_PATH}`);
	}
	return ensureClockwiseExterior(match);
}

function countPoints(geometry) {
	const walk = (coords) =>
		typeof coords[0] === 'number' ? 1 : coords.reduce((sum, c) => sum + walk(c), 0);
	return walk(geometry.coordinates);
}

function simplifyFeature(districtFeature) {
	const topo = presimplify(topology({ district: districtFeature }));
	const minWeight = quantile(topo, KEEP_FRACTION);
	const simplified = simplify(topo, minWeight);
	return feature(simplified, simplified.objects.district);
}

function projectToPath(simplifiedFeature) {
	const projection = geoMercator().fitSize([FIT_SIZE, FIT_SIZE], simplifiedFeature);
	const pathGenerator = geoPath(projection).digits(COORD_PRECISION);
	const d = pathGenerator(simplifiedFeature);
	if (d === null) {
		throw new Error('Path generator returned null for NC-12');
	}
	const round = (n) => Math.round(n * 10) / 10;
	const [[x0, y0], [x1, y1]] = pathGenerator.bounds(simplifiedFeature);
	const viewBox = {
		minX: round(x0),
		minY: round(y0),
		width: round(x1 - x0),
		height: round(y1 - y0)
	};
	return { d, viewBox };
}

function renderTypeScript({ d, viewBox }) {
	return [
		'// Auto-generated by scripts/generate-nc12-path.mjs — do not hand-edit.',
		'// To regenerate: `pnpm gen:nc12` from apps/web/.',
		'',
		'export const NC12_VIEWBOX = {',
		`\tminX: ${viewBox.minX},`,
		`\tminY: ${viewBox.minY},`,
		`\twidth: ${viewBox.width},`,
		`\theight: ${viewBox.height}`,
		'} as const;',
		'',
		`export const NC12_PATH = ${JSON.stringify(d)};`,
		''
	].join('\n');
}

function signedArea(ring) {
	let area = 0;
	for (let i = 0; i < ring.length - 1; i++) {
		const [x1, y1] = ring[i];
		const [x2, y2] = ring[i + 1];
		area += x1 * y2 - x2 * y1;
	}
	return area / 2;
}

function ensureClockwiseExterior(districtFeature) {
	const exteriorRing = districtFeature.geometry.coordinates[0];
	if (signedArea(exteriorRing) > 0) {
		exteriorRing.reverse();
	}
	return districtFeature;
}

function main() {
	const districtFeature = loadDistrictFeature();
	const before = countPoints(districtFeature.geometry);

	const simplifiedFeature = simplifyFeature(districtFeature);
	const after = countPoints(simplifiedFeature.geometry);

	const result = projectToPath(simplifiedFeature);

	mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
	writeFileSync(OUTPUT_PATH, renderTypeScript(result), 'utf-8');

	console.log(`NC-12: ${before} → ${after} points (${((100 * after) / before).toFixed(1)}%)`);
	console.log(`viewBox: ${result.viewBox.width} × ${result.viewBox.height}`);
	console.log(`Wrote ${OUTPUT_PATH}`);
}

main();
